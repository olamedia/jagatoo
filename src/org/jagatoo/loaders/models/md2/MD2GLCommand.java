package org.jagatoo.loaders.models.md2;

import java.io.IOException;

import org.jagatoo.util.streams.LittleEndianDataInputStream;

import org.openmali.vecmath2.TexCoord2f;

/**
 * A single GL command within a MD2 File.
 * 
 * @author Kevin Glass
 */
public class MD2GLCommand
{
    /** The texture mapping coordinates associated with this command */
    private TexCoord2f coords = new TexCoord2f();
    /** The index into the vertex array of this command */
    private int vertex; 
    
    /** 
     * Creates new MDGLCommand. 
     * 
     * @param in The input stream to read this gl command from
     */
    public MD2GLCommand( LittleEndianDataInputStream in ) throws IOException
    {
        coords.setS( in.readFloat() );
        coords.setT( 1.0f - in.readFloat() );
        vertex = in.readInt();
    }
    
    /**
     * Retrieves the texture coordinates.
     * 
     * @return The texture coordinates
     */
    public TexCoord2f getTextureCoordinates()
    {
        return( coords );
    }
    
    /**
     * Retrieves the defined vertex index.
     * 
     * @return The vertex index
     */
    public int getVertexIndex()
    {
        return( vertex );
    }
}
