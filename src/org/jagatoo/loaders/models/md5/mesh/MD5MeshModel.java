package org.jagatoo.loaders.models.md5.mesh;

import java.util.Vector;

/**
 * @author kman
 */
public class MD5MeshModel
{
    protected String name;
    protected MD5Skeleton skeleton;
    protected Vector< MD5Mesh > meshes = new Vector< MD5Mesh >();
    
    /**
     * Creates a new instance of MD5Model
     */
    public MD5MeshModel()
    {
    }
    
    public void setName( String name )
    {
        this.name = name;
    }
    
    public String getName()
    {
        return( name );
    }
    
    public void setSkeleton( MD5Skeleton skeleton )
    {
        this.skeleton = skeleton;
    }
    
    public MD5Skeleton getSkeleton()
    {
        return( skeleton );
    }
    
    public void setMeshes( Vector< MD5Mesh > meshes )
    {
        this.meshes = meshes;
    }
    
    public Vector< MD5Mesh > getMeshes()
    {
        return( meshes );
    }
    
    public void addMesh( MD5Mesh mesh )
    {
        if ( mesh.numvert > 0 )
            meshes.addElement( mesh );
    }
}
